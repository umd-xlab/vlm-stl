    def instantiate_state_data(self):
        """
        Creates empty dictionary of variable states to be plotted on graphs

        """
        for var in [self.input, self.output]:
            if var is not None:
                self.state_data[var] = ([],[])


    def update(self, line: pd.DataFrame):
        """
        Called from the start_overseer function whenever it detected a new line in the feed

        Determines what variables (that exist in this rule) are reported in this new line of data.
        Not all states reported in every line, therefore checks which variables are actually populated
        If any of those variables are populated, extracts their data and appends their values to their respective state data and update instance
        Sends the update instance through the rtamt library
        Upon receiving the output from the rtamt library, if new output robustness values reported,
        Sends the data to the router, appends to the rule_data points, then calls an update on the graphs

        """
        vars_to_update = []
        #Determines which variables are populated
        for var in [self.input, self.output]:
            if var != None:
                if not pd.isna(line.iloc[0][var]):
                    vars_to_update.append(var)
        
        updates = []
        #Updates the variable states dictionary and prepares update message for rtamt
        for var in vars_to_update:
            time = float(line.iloc[0]['time'])
            signal = float(line.iloc[0][var])
            self.state_data[var][0].append(time)
            self.state_data[var][1].append(signal)
            updates.append([var, [[time, signal]]])

        #send the update message into rtamt and receives output
        rob = self.spec.update(*updates)

        #Upon reception, rob will be a list of lists ex: [[5.4, inf], [6.1, -0.7]]
        #List is looped, rule_data points updated, calls update function for graphs
        for i in range(0, len(rob)):
            self.route_data(rob[i])
            self.rule_data[0].append(rob[i][0])
            self.rule_data[1].append(rob[i][1])
            self.update_graph()

    def route_data(self, output):
        """
        Called from the update function, responsible for sending data to logs
        output - singular output robustness result ex: [5.4, inf]

        Determines whether the output robustness value represents a violation
        If so, sends data the failure_log as well

        """
        time = output[0]
        robustness = output[1]
        if robustness < 0:
            with open (self.failure_log, mode='a') as f:
                writer = csv.writer(f)
                writer.writerow([self.rule, time, robustness])
        with open(self.data_dump_log, mode='a') as f:
            writer = csv.writer(f)
            writer.writerow([self.rule, time, robustness])

    def update_graph(self):
        """
        Called from the udpate function, resposnsible for updating the graphs

        Clears the current graphs to force an update and repopulates them
        """
        self.graph.clear()
        #Step functions, to accurately represent the assumption of constant signal until reported otherwise
        self.graph.step(*self.rule_data, where='post', label = 'Rule Output Robustness')
        for var in self.state_data:
            #Loads in rule variables state data to assist with validation
            self.graph.step(*self.state_data[var], where='post', label = f'{var}', alpha=0.5)

        #Shading logic, used to represent the infinities given by output robustness (aka when a rule is vacously true)
        start_shading = None
        for (x,y) in zip(self.rule_data[0], self.rule_data[1]):
            if y == float('inf') and start_shading is None:
                start_shading = x
            elif y != float('inf') and start_shading is not None:
                self.graph.axvspan(start_shading, x, color='gray', alpha=0.25)
                start_shading = None

        #Includes grid, title, legend, and centers the graph
        self.graph.grid(True, which='both')
        self.graph.set_title(f"Rule: {self.rule}", wrap=True)
        self.graph.axhline(y=0, color='k')
        self.graph.axvline(x=0, color='k')
        self.graph.legend()
        plt.tight_layout()
        #Saves the graph name based on rule, uses 3rd party library for text cleaning: https://www.tradingcode.net/python/sanitise-clean-filename/
        clean_name = sanitize_filename(self.rule, replacement_text="_")
        self.fig.savefig(f"plots/{clean_name}.png")

    def start_overseer(self):
        """
        Called by the activate function, responsible for montioring the feed, and reporting when updates has occured

        Contains internal functions (count_lines and on_modified) responsible for determining when an actual update has occurred and trigger update function
        Creates and activates an Observer, which waits until the stop_event is "set" before terminating

        Inspirationd drawn from the following sources:
        https://stackoverflow.com/questions/62412482/how-to-monitor-a-csv-file-for-changes
        https://www.geeksforgeeks.org/python/create-a-watchdog-in-python-to-look-for-filesystem-changes/

        """
        current_lines = 0

        #Called by event handler when Observer is triggered
        #If the trigger was because there is a new line (i.e more lines than previosuly counted), call update function
        def on_modified(event):
            cur_iter = count_lines()
            nonlocal current_lines
            if cur_iter > current_lines:
                current_lines = cur_iter
                df = pd.read_csv(event.src_path)
                if current_lines != 1: #Skips the header
                    self.update(df[-1:])
        
        #Counts the number of lines in the csv, used to determine if an update actually occurred
        def count_lines():
            total=0
            with open('feed.csv', 'r') as f:
                for line in f:
                    total+=1
            return total

        #Creates the event handler and links to the on_modified function
        event_handler = PatternMatchingEventHandler(patterns=['feed.csv'], ignore_patterns=[], ignore_directories=True, case_sensitive=True)
        event_handler.on_modified = on_modified

        #Instnatiates the observer and starts a new thread for it
        #Waits for stop_event to be triggered, then shutsdown
        observer = Observer()
        observer.schedule(event_handler, path=".", recursive=False)
        observer.start()

        self.stop_event.wait()
        observer.stop()
        observer.join()

    def activate(self):
        """
        Activation function, called from booter.py by each thread to start the observer
        """
        self.start_overseer()



