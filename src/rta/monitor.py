class Monitor:
    def __init__(self, feed_path, rule_line, stop_event, failure_log, data_dump_log):
        """
        The heart of the run time assurance (rta)
        Many instances (num = # of rules) booted up in parallel inside booter.py

        Responsible for monitoring the feed for robot_states that match its rule
        Feeds matching states through update function which generates robustness values
        Robustness values then get sent to log files (failure_log and/or data_dump_log)
        Robustness values also synchronosouly plotted in plots folder

        feed_path - (string) file path to where the robot state data will come in
        rule - (string), to be popualted in add_rule function, contains the stl rule
        input - (string), to be populated in add_rule function, contains input variables for rule
        output - (string), to be populated in add_rule function, contains output variables for rule
        spec - (rtamt object), used to pass in rule instantiation and updates into rtamt library
        stop_event - internal variable, used for parallel processing, passed through to all classes
        failure_log - (string) file path where to send all stl rule violations
        data_dump_log - (string) file path to where to send all outputed robustness values for stl rules
        fig and graph - just variable substitutes for the typical fig,ax schema, need to make subplots to be able to save graph
        rule_data - (tuple of lists) containing the time and output robustness value for the rule, appended to every time new output is generated by rtamt
        state_data - (dictionary of tuple of lists) ex: {'spd' : ([1,2,3],[4,5,6])} each tuple contains time and signal respectively for each state variable
            used by rule. To be plotted on graphs to help with output robustness analysis

        """
        self.feed_path = feed_path
        self.rule = None
        self.input = None
        self.output = None
        self.spec = None
        self.stop_event = stop_event
        self.failure_log = failure_log
        self.data_dump_log = data_dump_log
        self.fig, self.graph = plt.subplots()
        self.rule_data = ([],[]) # tuple containing x and y values of generated rule robustness
        self.state_data = {} #dictionary containing state datapoints for each variable

        self.add_rule(rule_line)
        self.instantiate_rule()
        self.instantiate_state_data()


    def add_rule(self, line: list):
        """
        Passed through with rule_line variable in init
        line - (list of strings), sent over by booter.py, contains 3 columns for "rule", "input" varibale, and "output" variable

        Indexes into list extracting comonents and setting them to variables, creates empty rtamt instance
        """
        self.rule = line[0]
        self.input = line[1]
        if self.input == '':
            self.input = None
        self.output = line[2]
        self.spec = rtamt.StlDenseTimeSpecification(semantics=rtamt.Semantics.OUTPUT_ROBUSTNESS) #defualt is standard robustness

    def instantiate_rule(self):
        """
        Uses values extracted in add_rule function to set propper parameters
        Then calls standard rtamt functions of parse and pastify to activate rule

        NOTE: pastify is needed to evaluate future timed temporal operators such as "eventually"

        """
        self.spec.declare_var('out', 'float')
        if self.input != None:
            self.spec.declare_var(str(self.input), 'float')
            self.spec.set_var_io_type(str(self.input), 'input')
        if self.output != None:
            self.spec.declare_var(str(self.output), 'float')
            self.spec.set_var_io_type(str(self.output), 'output')

        self.spec.spec = self.rule.strip("'") #removes excess single quotes (') from rule

